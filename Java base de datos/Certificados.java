public class Certificados {

    private int idCertificado;
    private int idEmpleado;
    private int NumCertificado;
    private String EstadoCertificado;

    public int getIdCertificado() {
        return idCertificado;
    }

    public void setIdCertificado(int idCertificado) {
        this.idCertificado = idCertificado;
    }

    public int getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(int idEmpleado) {
        this.idEmpleado = idEmpleado;
    }

    public int getNumCertificado() {
        return NumCertificado;
    }

    public void setNumCertificado(int numCertificado) {
        NumCertificado = numCertificado;
    }

    public String getEstadoCertificado() {
        return EstadoCertificado;
    }

    public void setEstadoCertificado(String estadoCertificado) {
        EstadoCertificado = estadoCertificado;
    }

}

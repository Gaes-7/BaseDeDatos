public class Ciudad {

    private int idCiudad;
    private String NomCiudad;

    public int getIdCiudad() {
        return idCiudad;
    }

    public void setIdCiudad(int idCiudad) {
        this.idCiudad = idCiudad;
    }

    public String getNomCiudad() {
        return NomCiudad;
    }

    public void setNomCiudad(String nomCiudad) {
        NomCiudad = nomCiudad;
    }

}
